def greaterThan(x,y):
    if x > y:
        return  True
    else:
        return False
def smallerThan(x,y):
    if x < y:
        return True
    else:
        return False
def equalTo(x,y):
    if x == y:
        return True
    else:
        return False
def greaterOrEqual(x,y):
    if x >= y:
        return True
    else:
        return False
def smallerOrEqual(x,y):
    if x <= y:
        return True
    else:
        return False 